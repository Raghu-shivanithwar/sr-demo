/* Detect crop id from current filename: cotton.html -> 'cotton'
   Returns null on index or when not a crop page.
*/
function detectCropId() {
  const path = window.location.pathname || window.location.href;
  const file = path.split('/').pop().split('?')[0].toLowerCase();
  if (!file || file === '' || file === 'index.html' || file === '/') return null;
  return file.replace('.html', '');
}

