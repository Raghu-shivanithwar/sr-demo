/* Detect crop id from current filename: cotton.html -> 'cotton'
   Returns null on index or when not a crop page.
*/
function detectCropId() {
  const path = window.location.pathname || window.location.href;
  const file = path.split('/').pop().split('?')[0].toLowerCase();
  if (!file || file === '' || file === 'index.html' || file === '/') return null;
  return file.replace('.html', '');
}

/* Convert language code to display name */
function languageName(code) {
  return ({ en: 'English', hi: 'हिंदी', te: 'తెలుగు', ta: 'தமிழ்', mr: 'मराठी', kn: 'ಕನ್ನಡ' })[code] || code;
}

/* Capitalize first letter of a string */
function capitalize(s) {
  if (!s) return s;
  return s.charAt(0).toUpperCase() + s.slice(1);
}

/* -------------------------
   Show manual content
   crop: cropId string
   lang: language code string
   ------------------------- */
function showManual(crop, lang) {
  const manualSection = document.getElementById('manual-section');
  const titleEl = document.getElementById('manual-title');
  const contentEl = document.getElementById('manual-content');
  const breadcrumbEl = document.getElementById('breadcrumb');

  const cropData = MANUALS[crop];
  const manualData = cropData?.[lang];
  if (!manualData) {
    titleEl.textContent = 'Manual';
    contentEl.innerHTML = '<p>Manual not available in this language yet.</p>';
    breadcrumbEl.innerHTML = `Home › ${capitalize(crop)} › ${lang}`;
  } else {
    const m = manualData;
    titleEl.textContent = m.title;
    contentEl.innerHTML = m.content;
    breadcrumbEl.innerHTML = `Home › ${capitalize(crop)} › ${languageName(lang)}`;
  }

  // Make manual section visible with fade-in
  // First make it visible but keep it transparent
  manualSection.style.display = 'block';

  // Force a reflow to ensure display:block is applied before transition
  manualSection.getBoundingClientRect();

  // Then trigger the fade-in animation
  setTimeout(() => {
    manualSection.classList.add('show');
  }, 10);
}

/* -------------------------
   Setup language card click handlers
   ------------------------- */
function setupLanguageCards() {
  const cards = document.querySelectorAll('.language-card');
  cards.forEach(card => {
    card.addEventListener('click', () => {
      const lang = card.dataset.lang;
      const crop = detectCropId();
      if (!crop) return;
      showManual(crop, lang);
      // Smooth scroll to manual area
      const manualSection = document.getElementById('manual-section');
      if (manualSection) {
        manualSection.scrollIntoView({ behavior: 'smooth', block: 'start' });
      }
    });
  });
}

/* -------------------------
   DOM ready / Initialization
   ------------------------- */
document.addEventListener('DOMContentLoaded', function () {
  // Initialize Materialize components (sidenav)
  if (window.M && M.Sidenav) {
    const elems = document.querySelectorAll('.sidenav');
    M.Sidenav.init(elems);
  }

  // Setup handlers for language selection
  setupLanguageCards();

  // If the page URL has ?lang=xx (e.g. cotton.html?lang=hi), auto open manual
  const urlParams = new URLSearchParams(window.location.search);
  const langParam = urlParams.get('lang');
  
  const crop = detectCropId();
  // If crop page — prefer explicit ?lang param; otherwise default to English
  if (crop) {
    const langToShow = langParam || 'en';
    // small delay in case DOM layout is still happening
    setTimeout(() => showManual(crop, langToShow), 120);
  }
});
